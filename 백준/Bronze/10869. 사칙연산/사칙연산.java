import java.util.Scanner;

public class Main {
	
	public static void main(String[] args) {
		
		Scanner n = new Scanner(System.in);
		int A = n.nextInt();
		int B = n.nextInt();
		
		System.out.println(A + B);
		System.out.println(A - B);
		System.out.println(A * B);
		System.out.println(A / B);
		System.out.println(A % B);
		
	}
}